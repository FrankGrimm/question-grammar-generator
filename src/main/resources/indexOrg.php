<p><em>Workshop + hackathon in conjunction with <a href="https://2021.eacl.org">EACL 2021</a>.</em></p>



<h6>Link: <a href="http://embeddia.eu/hackashop2021-programme/">Hackashop workshop programme</a></h6>



<h6>Link: <a href="https://www.aclweb.org/anthology/volumes/2021.hackashop-1/" data-type="URL" data-id="https://www.aclweb.org/anthology/volumes/2021.hackashop-1/">Official Hackashop proceedings in the ACL Anthology</a></h6>



<div class="wp-block-group"><div class="wp-block-group__inner-container">
<p style="padding-left: 40px;"><a href="https://www.aclweb.org/anthology/volumes/2021.hackashop-1/"><em data-rich-text-format-boundary="true">Proceedings of the EACL Hackashop on News Media Content Analysis and Automated Report Generation</em></a>, 19 April 2021. Hannu Toivonen and Michele Boggia, editors. Association for Computational Linguistics. ISBN 978-1-954085-13-8.</p>
<p style="padding-left: 40px;">The proceedings include <a href="https://www.aclweb.org/anthology/2021.hackashop-1.0.pdf">13 peer-reviewed workshop papers, five hackathon reports</a> and <a href="https://www.aclweb.org/anthology/2021.hackashop-1.14.pdf">a description of relevant resources (tools, models, data, challenges)</a>. </p>



<p></p>
</div></div>



<h6>Hackashop description</h6>



<p>Automated content analysis of news media, including both news articles and users’ comments on them, can provide unparalleled insight into current events, interests and opinions, as well as trends and changes in them. The needs are varied, from the readers who consume news of their personal interest to journalists who keep track of what is going on in the world, try to understand what their readers think of various topics, or want to automate routine reporting. </p>



<p>The aim of the hackashop is to foster discussion and research on the combination of language technology and news media content. The hackashop is a forum for both (1) discussing scientific advances in analysis of news stories and their reader comments and in automated generation of reports, as well as for (2) experimental work on identifying interesting phenomena in reader comments and reporting on them.</p>



<p>We embrace cross-disciplinary collaborations of computer scientists with media researchers and other social scientists in order to reach richer insights into the needs and opportunities in news media analysis and generation. We welcome contributions that address multilingual settings, including low-resource languages.</p>



<p>The hackashop implements a novel, dual format: (1) a traditional track with paper submissions, reviews and paper presentations, and (2) an active, experimentation-based track where hackathon-type online activities precede the workshop, and hackathon teams/individuals present their work in the workshop. Datasets and a suite of tools for optional use in the hackathon are provided by the organizers. Participants can choose to participate in only one or in both roles. Both formats had their own calls for papers and participation (see below).</p>



<h6>Organizing team</h6>



<p>Hannu Toivonen (University of Helsinki, Finland), Chair<br>Matthew Purver (Queen Mary University of London, UK)<br>Senja Pollak (Jozef Stefan Institute, Slovenia)<br>Nada Lavrač (Jozef Stefan Institute, Slovenia)<br>Marko Robnik-Šikonja (University of Ljubljana, Slovenia)<br>Michele Boggia (University of Helsinki, Finland)</p>



