	<h2>EnerG - English Neologisms Research Group</h2>
	
	A <a href="http://www.anglistik.uni-muenchen.de/abteilungen/sprachwissenschaft/research/research_projects1/dfg-projekt/index.html" target="top">research group</a> dedicated to investigating the processes involved in the diffusion and entrenchment of new English words and expressions.:q!

	<br><br>
	
	Lead by <a href="http://www.anglistik.uni-muenchen.de/personen/professoren/schmid/index.html" target="top">Philipp Cimiano</a><br><br><br>
	
	<p><strong>Words, words, words</strong></p>
	
	<p>English is the language with not only the largest, but also the most dynamic and fastest growing lexicon. New words are coined and used literally every day in all corners of the English-speaking world. While some of these new words meet the need to name newly invented objects and innovative ideas, speakers also come up with novel words and expressions because they want to render observed social practices, activities or ways of thinking linguistically more tangible. Good examples are the notion of <i>locavore</i> or <i>localvore</i>, referring to people who prefer to buy food grown and produced nearby, and the fairly recent coinage of <i>sodcasting</i>, meaning ‘playing music on a mobile phone in a public place’. Often, playing with language and getting across personal attitudes are important motivations for using a new word. Consider once more the example of <i>sodcasting</i>, which clearly has the negative connotation that this practice can be rather annoying for bystanders.</p>
	
	<p><strong>The career of new words</strong></p>
	
	<p>Of course, not all new coinages are here to stay. Indeed many ad-hoc formations, which are created on the fly, are never used a second time – their occurrence may not even be noticed. Others do catch on, however, and begin to spread across the speech community. New words in this early phase of their life cycle are called neologisms. The process of the diffusion of neologisms in the speech community can be almost instantaneous, e.g. when a key public figure uses a new expression which is spread by the media and immediately taken up by the general public, but it can also be gradual, with more and more speakers of English being confronted with and using the word in more and more diverse contexts.</p>
	
	<p><strong>What are the factors determining the success of neologisms?</strong></p>
	
	<p>The aim of the EnerG project is to investigate the factors influencing the success and failure of neologisms. Questions addressed include the following:</p>
	
	<ul>
	    <li>What are the circumstances under which new words are coined? What are the coiners’ motives and aims?</li>
	    <li>Which social factors and processes foster and hinder the diffusion of new English words and expressions?</li>
	    <li>What are the cognitive factors involved in the entrenchment of new words in the minds of the individual members of the speech community?</li>
	    <li>How do cognitive and social factors interact?</li>
	    <li>What is the effect of the morphological makeup of a new word on its appeal and chance of success?</li>
	    <li>What role do recurrent linguistic patterns around the new words, for example collocations, play in their entrenchment and diffusion?</li>
	    <li>What happens if several quasi-synonymous neologisms compete for acceptance by the speech community (e.g. <i>locavore</i> vs. <i>localvore</i>, <i>google cooking</i> vs. <i>fridge googling</i>)?</li>
	</ul>
	
	<p><strong><br>How does EnerG proceed?</strong></p>
	
	<p>The ideal method for investigating these questions would of course follow classic dialectological practice: send out as many field workers as possible, collect as much material and conduct as many interviews as you can. However, since this is way beyond our means, we use the Internet as a handy proxy for investigating what speakers of English do with their language. We are of course aware both of the drastic limitations that this approach has and of the tremendous challenge posed by the messiness of the material on the world wide web, but we believe that it is still a viable option. After all, more and more new expressions are born or deliberately launched in blogs and on websites on the Internet, so searching for their spread in this medium is a natural choice.</p>
	<p>In order to systematically monitor the spread of neologisms on the Internet, we have created a tailor-made software, the so-called NeoCrawler (for details see Kerremans, Stegmayr and Schmid 2012, and Kerremans Ms.). This tool basically has two components: the Discoverer, which allows us to spot new words on the web as soon as possible after their first appearance, and the Observer, which carries out searches at regular intervals to monitor the dynamics of the use of the given neologims on the Internet across time. All occurrences of new words on the pages found are further processed with special linguistic software that allows us to quickly form an idea of how the word is used in context, which linguistic patterns prevail and how they change across time. The linguistic data we collect plus all available information about contexts, users, subject-areas, types of Internet sources etc. are stored in a large database.</p>
	<p>In the early phases of the project, the neologisms investigated were selected in consultation with the <i>Oxford English Dictionary</i>, which hosts the largest database of recently coined English words in the world. Since the implementation of the Discoverer in 2011, our work has the potential to contribute to the ongoing revision and updating of the Oxford English Dictionary by indentifying recent coinings and their first attested uses on the web and beyond, by providing evidence for (or indeed against) the spread of words from highly restricted areas (e.g. a single blog) to more diverse Internet sources, and by collecting typical quotations (cf. Durkin 2012: 103).</p>
	<p>At a later stage of the project, the investigation of the diffusion of neologisms on the Internet will be complemented by (neuro-)psychological experiments probing into the cognitive aspects of the entrenchment of new words.</p>
	
